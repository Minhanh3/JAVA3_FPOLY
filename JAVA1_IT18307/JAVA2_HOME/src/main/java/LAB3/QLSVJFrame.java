    private SinhVien getForm()
    {
        String hoTen = this.txtName.getText();
        String diem = this.txtDiem.getText();
        
        String hocLuc = this.txtHocLuc.getText();
        String nganh = this.cbxNganh.getSelectedItem().toString();
        String thuong = this.chkThuong.isSelected() == true
                ? "Có phần thưởng" : "Không có phần thưởng";
        
        SinhVien sv = new SinhVien(hoTen, diem, nganh, hocLuc, thuong);
        return sv; 
    }
    
    private void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblStudent.getModel();
        dtm.setRowCount(0); // Xóa các dòng đang có trên JTable
        for (SinhVien sv : dssv) {
            Object[] object = {
                sv.getHoTen(),
                sv.getDiem(),  
                sv.getNganh(),
                sv.getHocLuc(),
                sv.getThuong()
            };
            dtm.addRow(object);
        } // Kết thúc for

    }
    
    
    
