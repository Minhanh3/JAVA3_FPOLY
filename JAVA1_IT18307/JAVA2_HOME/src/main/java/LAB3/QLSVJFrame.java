    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        txtName.setText(null);
        txtHocLuc.setText(null);
        txtDiem.setText(null);
        cbxNganh.setSelectedIndex(0);
        chkThuong.setSelected(false);
    }//GEN-LAST:event_btnResetActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String hoTen = this.txtName.getText();
        String diem = this.txtDiem.getText();
        
        String hocLuc = this.txtHocLuc.getText();
        String nganh = this.cbxNganh.getSelectedItem().toString();
        String thuong = this.chkThuong.isSelected() == true
                ? "Có phần thưởng" : "Không có phần thưởng";
        
        SinhVien sv = new SinhVien(hoTen, diem, nganh, hocLuc, thuong);
        dssv.add(sv);
        loadTable();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        int row = this.tblStudent.getSelectedRow();
        if (row == -1) {
            return ;
        }
        
        this.dssv.remove(row);
        this.loadTable();
    }//GEN-LAST:event_btnDelActionPerformed
    
    
    //UPDATE CHƯA HOÀN THIỆN
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = this.tblStudent.getSelectedRow();
        if (row == -1) {
            return ;
        }
        
        
        String hoTen = this.txtName.getText();
        String diem = this.txtDiem.getText();
        String nganh = this.cbxNganh.getSelectedItem().toString();
        String hocLuc = this.txtHocLuc.getText();
     
        String thuong = this.chkThuong.isSelected() == true
                ? "Có phần thưởng" : "Không có phấn thưởng";
        
        SinhVien sv = new SinhVien(hoTen, diem, nganh, hocLuc, thuong);
        
        dssv.set(row, sv);
        this.loadTable();
    }//GEN-LAST:event_btnUpdateActionPerformed
    
    
    
    private void tblSVMouseClicked(java.awt.event.MouseEvent evt) {                                   
        int row = this.tblStudent.getSelectedRow();
        if (row == -1) {
            return ;
        }
        
        SinhVien sv = this.dssv.get(row);
        this.txtName.setText( sv.getHoTen());
        this.txtDiem.setText( sv.getDiem());
        this.txtHocLuc.setText( sv.getHocLuc());      
        boolean stt = sv.getThuong().equals("Có phần thưởng") ? true : false;
        this.chkThuong.setSelected(stt);
        this.cbxNganh.setSelectedItem(sv.getNganh());
    } 
    
    
    
