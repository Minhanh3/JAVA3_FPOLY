    public void loadform(ArrayList<SanPham> Listsp) {
        dtm.setRowCount(0);
        for (SanPham sP : Listsp) {
            dtm.addRow(new Object[]{
                sP.getMa(),
                sP.getTen(),
                sP.getDvt(),
                sP.getGia(),
                sP.getCungcap()
            });
        }
    }

